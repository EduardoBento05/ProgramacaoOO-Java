package exercicio05;

public class Artista {
	
	private String nomeArtista;
	
	private String nacionalidadeArtista;
	
	private String biografiaArtista;
	
	
	public Artista(String nomeArtista, String nacionalidadeArtista, String biografiaArtista) {
		this.nomeArtista = nomeArtista;
		this.nacionalidadeArtista = nacionalidadeArtista;
		this.biografiaArtista = biografiaArtista;
	}


	public String getNomeArtista() {
		return nomeArtista;
	}


	public String getNacionalidadeArtista() {
		return nacionalidadeArtista;
	}


	public String getBiografiaArtista() {
		return biografiaArtista;
	}
	
	


	
	
	

}
